# Variables in Python

# Variable used to store data values
x = 10
name = "Rama"

# 'x' and 'name' are variables
# Values 10 and "Rama" are stored in them
# '=' is an assignment operator (assigns value to a variable)

print("The student number is", x)
print("The student name is", name)
print(x, name)  # Print multiple variables with comma separation

# Formatted string (f-string)
print(f"The student name is {name} and the roll number is {x}")

# Check the type of data stored in variables
print(type(x))      # int
print(type(name))   # str

# Follow naming conventions when creating variables