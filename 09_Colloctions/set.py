#Set is a collection which is unordered and unindexed. No duplicate members.
# Sets are defined by placing items inside curly braces {}.
# Example usage:
my_set = {1, 2, 3, "apple", "banana", True, 3.14}
print(my_set)  # Output: {1, 2, 3, 'apple', 'banana', True, 3.14}
my_set1 = {501, 502, 503, "Ramu", "Somu", "Krishan", 80.5, 90.5, 100.5}
print(my_set1)  # Output: {501, 502, 503, 'Ramu', 'Somu', 'Krishan', 80.5, 90.5, 100.5}