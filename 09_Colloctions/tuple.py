#Tuple is a collection which is ordered and unchangeable. Allows duplicate members.
# Tuples are defined by placing items inside parentheses ().
# Example usage:
my_tuple = (1, 2, 3, "apple", "banana", True, 3.14)
print(my_tuple)  # Output: (1, 2, 3, 'apple', 'banana', True, 3.14)
my_tuple1 = (501, 502, 503, "Ramu", "Somu", "Krishan", 80.5, 90.5, 100.5)
print(my_tuple1)  # Output: (501, 502, 503, 'Ramu', 'Somu', 'Krishan', 80.5, 90.5, 100.5) 
  